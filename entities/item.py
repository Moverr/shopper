class Item(object):
    def __init__(self):
        pass

    def setCategoryId(self,category_id):         
        self.category_id = category_id

    def getCategoryId(self):
        return  self.category_id 

    def setName(self,name):
        self.name = name
    
    def getName(self):
        return  self.name

    def setCode(self,code):
        self.code = code
    
    def getCode(self):
        return  self.code

    def setStatus(self,status):
        self.status = status
    
    def getStatus(self):
        return  self.status

     



    
    
        