class Contact(object):
    def __init__(self):
        pass
 
    def setType(self,contacttype):
        self.contacttype = contacttype
    
    def getType(self):
        return  self.contacttype

    def setDetails(self,details):
        self.details = details
    
    def getDetails(self):
        return  self.details

    def setStatus(self,status):
        self.status = status
    
    def getStatus(self):
        return  self.status

     



    
    
        